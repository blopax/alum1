#include "alum1.h"

t_bool	ft_parse_play(char *str)
{
	if (ft_strlen(str) != 2)
		return (FALSE);
	if (str[0] != '1' || str[0] != '2' || str[0] != '3')
		return (FALSE);
	return (TRUE);
}
